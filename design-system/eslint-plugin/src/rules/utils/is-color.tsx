const includesWholeWord = (value: string, options: Set<string>) => {
  const values = value
    .replace(/[^a-zA-Z ]/g, ' ')
    .trim()
    .split(/(?:,|\.| )+/);
  return values.some((value) => options.has(value));
};

export const isLegacyColor = (value: string) => legacyColors.has(value);
export const isLegacyNamedColor = (value: string) =>
  legacyColorMixins.has(value);

export const includesHardCodedColor = (raw: string): boolean => {
  const value = raw.toLowerCase();

  if (
    /#(?:[a-f0-9]{3}|[a-f0-9]{6}|[a-f0-9]{8})\b|((?:rgb|hsl)a?|(lch|lab|color))\([^\)]*\)/.exec(
      value.toLowerCase(),
    )
  ) {
    return true;
  }

  if (includesWholeWord(value, namedColors)) {
    return true;
  }

  return false;
};

export const isHardCodedColor = (value: string): boolean => {
  if (includesWholeWord(value.toLowerCase(), namedColors)) {
    return true;
  }

  if (
    value.startsWith('rgb(') ||
    value.startsWith('rgba(') ||
    value.startsWith('hsl(') ||
    value.startsWith('hsla(') ||
    value.startsWith('lch(') ||
    value.startsWith('lab(') ||
    value.startsWith('color(')
  ) {
    return true;
  }

  if (
    value.startsWith('#') &&
    // short hex, hex, or hex with alpha
    (value.length === 4 || value.length === 7 || value.length === 9)
  ) {
    return true;
  }

  return false;
};
